              食用牧草可以有效的完成臼齒的水平磨牙，牧草內含有大量粗纖維，這些粗纖維進入腸胃道中可以促進腸胃道蠕動產生，減少疾病產生，這樣的進食動作和啃食磨牙棒和草磚的效果是不一樣的。因此永遠記住一句話，兔子正確的主食就是牧草。<br />

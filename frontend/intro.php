              兔子是ㄧ種草食性動物，顧名思義，就是以草為主食，目前把兔子當寵物看待的飼主，只要好好照顧，有正確的飼養管理方式，其壽命其實可以長達10年。兔子的終生飲食，就是要給飼料、牧草、水這3樣東西，缺一不可，只是隨著年紀的多寡，這3樣東西的比例要去挑選和調配。<br />

              但是鳳梨酵素卻沒有促進腸胃蠕動、幫助消化的功能喔。這是比較多人不了解的部分了。因此，如果兔子真的發生腸胃道堵塞的症狀，[水份]是最重要的，一但腸道水份不足，異物(可能是毛團或其他東西)要排出就更加困難。一定要立刻先去就醫，搭配藥物治療，在家裡可以幫兔兔做的保養，將鳳梨酵素和木瓜酵素約5-10顆磨成粉，泡ㄧ小瓶水約20-30cc左右，ㄧ天內分次給兔兔喝，還沒喝完的記得要冰起來，也不是一次就強迫兔子灌大量的水喔!因為兔兔此時通常有不吃東西、不喝水、沒有排謝的症狀了，要兔子自己進食比較困難，就需要灌水 (灌水的方式和使用小針筒餵藥的方式相同)。就算家裡沒有酵素也沒關係，就是要幫兔兔喝水。喝水很重要!<br /><br />
              每次少量少量讓兔兔加減喝點水，至少1-10CC 都可以，只要有喝都好，如果喝得少，就1-2小時餵一次，餵水的時間就要縮短，喝得多就可以拉長3-4小時再餵一次，主要是要保持腸道水份，量沒有一定要多少，飼主自行依照兔兔的情況拿捏即可，酵素不是藥物，所以沒劑量的問題，不需要擔心。<br /><br />
              備註：為什麼ㄧ次需要這麼多顆酵素磨粉呢? 根據醫師說明，因為坊間販售的寵物用酵素錠，廠牌不同，配方也都不同，每顆酵素單位量都不一定，為了使酵素真的能達到效果，所以需要一定量的酵素使用。<br /><br />
              如果兔兔開始會自己吃點食物喝水了，開始有一點便便了，就可以暫停餵食，讓兔兔自己進食比較好。<br /><br />

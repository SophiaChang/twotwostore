              不是獸醫都看兔子<br />
              在台灣的獸醫師養成體系，目前並沒有所謂的物種專業分科，而是採取通識教育原則，頂多根據學生的興趣在實習時根據目（寵物、農牧、保育、研究）而有所不同的體驗與接觸，因此每一位獸醫師在執業之後，對於貓或狗或兔的專業大多來自於執業醫院臨床案例、學長姐的傳承或自己的進修，因此即便有部份通識教育的基礎，但卻不是所有獸醫師都會幫寵物兔做正確的醫療。<br /><br />
              兔兔也是要看醫生的喔<br />
              兔兔是相當敏感的小動物，不同於貓貓狗狗會以聲音來向主人表達身體不適，安靜的兔兔通常都是默默的承受病痛，所以兔子的疾病需要主人投入相當的觀察力和經驗才能去了解兔兔的狀況。如果兔兔精神狀況變差、食慾不振或是大小便便不正常等等，通常都是生病了，這時千萬別病急亂投醫，請找專門的兔科醫生就診，以免投錯藥了結果病沒好，反而越來越糟糕喔！<br /><br />
              原來也有兔醫生<br />
              相對於常見的寵物狗、寵物貓這類雜食性動物，兔子這種草食性動物的身體構造與行為是跟貓狗完全不同的，因此即便是同樣的疾病在治療行為上也無法貓狗兔混用，這不是單純藥劑濃度比例的問題，而是整個物種結構與身體構造上的極大差異。目前台灣大多數的兔醫師來自於農牧場實習出生的野生動物醫師，部分來自於醫師對寵物兔的興趣而自行出國再進修，少部份是因地區特殊需求（獸醫院太少或去區域無人看兔），因此自然而然找了相關paper自修而成。一位有經驗的兔科醫師，對於前來看診的寵物兔會有相當自然且熟練的抱定(嬰兒抱或基本固定)行為，且對於醫療行為語就診過程中會有許多不同之處，這些細微面都是飼主在挑選醫師時需特別注意的。<br /><br />
